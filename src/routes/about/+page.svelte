				<h2 class="card-title">What is Carbon Intensity üåç</h2>
				<p>
					Carbon intensity is measured in grams of carbon dioxide (CO2) emitted per kilowatt-hour
					(gCO2/kWh) of electricity produced. This unit tells us how many grams of CO2 are released
					into the atmosphere for every kilowatt-hour of electricity we use. A lower number means
					cleaner electricity with fewer emissions, while a higher number indicates dirtier
					electricity with more emissions. By understanding and monitoring this unit, we can make
					more environmentally friendly choices about when to use electricity.
				</p>
			</div>
